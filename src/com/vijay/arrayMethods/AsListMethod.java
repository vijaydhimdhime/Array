package com.vijay.arrayMethods;

import java.util.Arrays;

public class AsListMethod {
	public static void main(String[] args) {
		String[] cities = {"pune","solapur","sangli","satara","kolhapur"};
		System.out.println("use aslist() : "+Arrays.asList(cities));
		String[] num = {"10","20","30"};
		System.out.println("use aslist() : "+Arrays.asList(num) );
	}
}
